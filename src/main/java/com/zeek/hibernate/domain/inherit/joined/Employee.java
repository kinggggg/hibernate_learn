package com.zeek.hibernate.domain.inherit.joined;

/**
 * 映射继承关系树--每个类对应一张表
 * @author weibo_li
 *
 */
public class Employee {
	private Integer id;
	private String name;
	private Integer age;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	
}
